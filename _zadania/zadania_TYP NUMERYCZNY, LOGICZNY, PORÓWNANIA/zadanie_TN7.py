# TN7 Sprawdź jaki będzie wynik działania operatora or i and przy porównaniu wartości:
# a) True, True
# b) False, True
# c) False, False
# d) [], 7
# e) 88, 0
# f) ‘d’, ‘x’

# a)
a = True and True
print(a)
a2 = True or True
print(a2)

# b)
b = False and True
print(b)
b2 = False or True
print(b2)

# c)
c = False and False
print(c)
c2 = False or False
print(c2)

# d)
d = [] and 7
print(d)
d2 = [] or 7
print(d2)

# e)
e = 88 and 0
print(e)
e2 = 88 or 0
print(e2)

# f)
f = 'd' and 'x'
print(f)
f2 = 'd' or 'x'
print(f2)
